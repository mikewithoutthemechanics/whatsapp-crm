# ── MAIN ─────────────────────────────────────────────────────────
def main():
    banner()
    env = load_env()

    env = setup_openwa(env)
    env = setup_ai(env)
    env = setup_admin(env)
    env = setup_database(env)

    save_env(env)

    ok_, _ = validate_env()

    if "docker" not in env and check_docker():
        start_now = ask_yesno("Start the Docker stack now?", default=True)
        if start_now:
            start_stack(env)
            import time as _t
            _t.sleep(5)
            ok_ = health_check()

    print(bold(green("\n✓  Setup complete!")) if ok_ else bold(yellow("\n⚠  Done — fix the errors above before starting.")))
    print_next_steps(env)


if __name__ == "__main__":
    main()
