# ── check Docker ────────────────────────────────────────────────
def check_docker() -> bool:
    try:
        subprocess.run(["docker", "--version"], capture_output=True, check=True)
        return True
    except (FileNotFoundError, subprocess.CalledProcessError):
        return False


# ── load / write .env ───────────────────────────────────────────
def load_env():
    env: dict = {}
    if ENV_PATH.exists():
        for line in ENV_PATH.read_text().splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                k, _, v = line.partition("=")
                env[k.strip()] = v.strip()
    return env


def save_env(env: dict):
    lines = ["# WhatsApp CRM SA — auto-generated by setup-production.py", ""]
    for k, v in env.items():
        lines.append(f"{k}={v}")
    ENV_PATH.write_text("\n".join(lines) + "\n")
    print(f"   {green('✓')}  Saved → {ENV_PATH}")


# ── prompter ────────────────────────────────────────────────────
def prompt(label: str, default: str = "", secret: bool = False) -> str:
    if secret:
        import getpass
        v = getpass.getpass(f"   {label}: ")
    else:
        v = input(f"   {label}{f' [{default}]' if default else ''}: ").strip()
    return v or default


def ask_yesno(label: str, default: bool = True) -> bool:
    yn = "Y/n" if default else "y/N"
    v = input(f"   {label} [{yn}]: ").strip().lower()
    if not v:
        return default
    return v in ("y", "yes")


# ── OpenWA section ──────────────────────────────────────────────
def setup_openwa(env: dict) -> dict:
    print(f"\n{yellow(bold('─── OpenWA Setup ─────────────────────────────────────────'))}")
    print("OpenWA = self-hosted WhatsApp gateway (free, no Meta approval).")
    print()

    running = ask_yesno("Do you already have OpenWA running somewhere?")
    if not running:
        print()
        print("  Start it now:")
        print(f"    {cyan(f'docker run -p {OPENWA_API_PORT}:{OPENWA_API_PORT} -p {OPENWA_DASH_PORT}:{OPENWA_DASH_PORT}')}")
        print(f"    {cyan(f'  -v openwa-data:/app/data --name crm-openwa {DOCKER_OPENWA_IMAGE}')}")
        print()
        input("  Press Enter when it's running and you've scanned the QR …")

    env["WHATSAPP_PROVIDER"]      = "openwa"
    env["OPENWA_SESSION_ID"]      = prompt("OpenWA session name", default="default")
    env["OPENWA_API_URL"]         = prompt("OpenWA API URL",
                                           default=env.get("OPENWA_API_URL",
                                                           f"http://localhost:{OPENWA_API_PORT}"))
    env["OPENWA_API_KEY"]         = prompt("OpenWA API key  (from Dashboard → Settings → API Access)",
                                           secret=True)
    env["OPENWA_HMAC_KEY"]        = prompt("OpenWA HMAC key  (optional — leave blank)",
                                           default=env.get("OPENWA_HMAC_KEY", ""))
    env["OPENWA_WEBHOOK_SECRET"]  = prompt("OpenWA webhook secret  (optional — leave blank)",
                                           default=env.get("OPENWA_WEBHOOK_SECRET", ""))
    print(f"   {green('✓')}  OpenWA configured")
    return env


